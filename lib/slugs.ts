// Kept for backwards compatibility — prefer ARTIST_CATALOG in new code
export const POPULAR_ARTISTS = ARTIST_CATALOG;

const nameToSlug = Object.fromEntries(
  Object.entries(ARTIST_CATALOG).map(([slug, name]) => [name, slug]),
);

export function slugToArtistName(slug: string): string {
  return ARTIST_CATALOG[slug] ?? slug.replace(/-/g, " ");
}

export function artistNameToSlug(name: string): string {
  return (
    nameToSlug[name] ??
    name
      .toLowerCase()
      .replace(/\s+/g, "-")
      .replace(/[^a-z0-9-]/g, "")
  );
}
